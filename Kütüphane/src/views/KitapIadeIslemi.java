    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        int uye_id= Integer.parseInt(jTextField1.getText());
        int kitapId = Integer.parseInt(jTextField4.getText());
        int oduncId = Integer.parseInt(jTextField5.getText());
        String kullaniciUnvan = kullanici.getUnvan();
        
        int gonderilenOgrenciId = 0;
        int gonderilenAkademisyenId = 0;
        if(kullaniciUnvan.equals("Öğrenci")){
            gonderilenOgrenciId = uye_id;
            
        }else if(kullaniciUnvan.equals("Akademisyen")){
            gonderilenAkademisyenId = uye_id;
        }
        
     
        
        Date oduncAlinmaTarihi = Date.valueOf(jTextField3.getText()); // Ödünç alınma tarihi

        OgrenciIslemleri ogrenciIslemleri = new OgrenciIslemleri();
         

        int sonuc = ogrenciIslemleri.iadeEtme(gonderilenOgrenciId, gonderilenAkademisyenId, kitapId, oduncId, oduncAlinmaTarihi);

        if (sonuc > 0) {
            ogrenciIslemleri.updateKitapDurumu(kitapId);
            // İade işlemi başarılı
            JOptionPane.showMessageDialog(this, "Kitap iade edildi.");
            // Tabloyu güncelle
            tabloyuDoldur();
        } else {
            // İade işlemi başarısız
            JOptionPane.showMessageDialog(this, "Kitap iade edilemedi.");
        }

        ogrenciIslemleri.kapat();

    }//GEN-LAST:event_jButton1MouseClicked

