     private void tabloyuDoldur() {
        DefaultTableModel model = (DefaultTableModel) kitapIadeTablosu.getModel();
        model.setRowCount(0); // Tabloyu temizle

        OgrenciIslemleri ogrenciIslemleri = new OgrenciIslemleri();
        
        ArrayList<Object[]> oduncAlinanKitaplar = ogrenciIslemleri.oduncAlinanKitaplariGetir();

        for (Object[] row : oduncAlinanKitaplar) {
            model.addRow(row);
        }

        ogrenciIslemleri.kapat();
    }
   
    
